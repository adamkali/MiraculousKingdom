            <h3>Getting Started</h3>
            <p>Before getting started there are a couple of things to keep in mind in order to keep the game fair and fun.</p>
            <ul> 
                <li> 
                    <h4>There is no Game Master... In a sense.</h4>
                    <p> 
                        You and your fellow players are all equal in this game, but there is a caviat to that.
                        Each round of the game is called an <b class="text-blue-700">Event</b>.
                        At the beginning of a game, each player generates a number out of 1-20.
                        The highest starts of the game and is the <b class="text-blue-700">Ruler</b> for the first <b class="text-blue-700">Event</b>.
                        The <b class="text-blue-700">Event</b> is determined by a table and a number corresponding to that number.
                        In the Pre-alpha release: there are 50 <b class="text-blue-700">Event</b>s, so generate a number from 1-50.
                        When the <b class="text-blue-700">Event</b> is found, have a number generated between 1 and 4 to determine how long the <b class="text-blue-700">Event</b> will last in the form of <b class="text-blue-700">Episode</b>s.
                        It is the <b class="text-blue-700">Ruler</b>'s job to set up how the <b class="text-blue-700">Event</b> fits with the story, and weave a tapestry of what happens in the story of the <b class="text-blue-700">Event</b>. 
                        The player to the left of the <b class="text-blue-700">Ruler</b> begins their turn. 
                        Once every player has had their turn, that is an <b class="text-blue-700">Episode</b>.
                        Once the <b class="text-blue-700">Episode</b> is over, the <b class="text-blue-700">Ruler</b> advances the story based on the actions taken by the players during the <b class="text-blue-700">Episode</b>.
                        Once the last <b class="text-blue-700">Episode</b> concludes. 
                        The player with the most <b class="text-blue-700">King's Favor</b> wins the <b class="text-blue-700">Event</b>, and gain's a <b class="text-blue-700">King's Favor</b>
                        Then the player to the right of the <b class="text-blue-700">Ruler</b> becomes the ruler and the cycle continues.
                    </p>
                </li>
                <li> 
                    <h4>What is a turn?</h4>
                    <p>
                        A turn can be any number of things. 
                        It could be using an ability, consorting with a player, activating a resource to get something done, or attacking someone.
                        You can think of this as a scene in the <b class="text-blue-700">Episode</b>. 
                        If you can see your character doing something in an episode of a drama, then it is an action.
                        If it is the current <b class="text-blue-700">Ruler</b>'s turn, then the player to the left of the <b class="text-blue-700">Ruler</b> becomes the <b class="text-blue-700">Ruler</b> for their turn.
                    </p>
                </li>
                <li> 
                    <h4>Creating a Setting</h4>
                    <p> 
                        The story you are telling should be interesting to all of the player's involved.
                        You should pick a setting where everyone can have fun roleplaying their character, and be able to get into the drama of the week so to speak.
                        Here are a couple of examples that could work:
                    </p>
                    <p> 
                        The players are advisors to a king in a medival world, each vying for greatness and perhaps the throne. Construct a story about what is happening in the world why you and your cohorts may be trying to take the thone, or win the effection of the ruler.
                    </p>
                    <p> 
                        The players are gangsters in the mob. You should set up what your illegal activity it, why you are at odds with the other players, or if there is a power vacum by the Don's side.
                    </p>
                    <p> 
                        The players are rappers ara a record label. Are you all in a group, or are you each trying to steal the show and become the richest artist in the world?
                    </p>
                    <p> 
                        Each of these examples answers a question about your setting: Who is the <b class="text-blue-700">Ruler</b>, and why do you want their favor (the ruler or the Don, the fans); who what is the end goal (to become the King or Queen, to become the Don, or To become the greatest rapper); and what is the <b class="text-blue-700">King's Favor</b>(How much the King or Queen likes your character, How much Crime you have committed, and How much money, or hits does your character have).
                    </p>
                    <p> 
                        At the end of the day you need to come up with: A name for the <b class="text-blue-700">Ruler</b>. And a name for your <b class="text-blue-700">Country</b> or the name of your show. These will be recorded on your character sheet.
                    </p>
                </li>
